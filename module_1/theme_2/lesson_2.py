def practice_1():
    # Ввод сторон прямоугольника (возможно, вещественных)
    a = float(input("Введите длину первой стороны прямоугольника: "))
    b = float(input("Введите длину второй стороны прямоугольника: "))

    # Вычисление площади и периметра
    area = a * b
    perimeter = 2 * (a + b)

    # Вывод результатов
    print("Площадь прямоугольника:", area)
    print("Периметр прямоугольника:", perimeter)


def practice_2():
    # Ввод пятизначного числа
    number = input("Введите пятизначное число: ")

    # Проверка длины
    if len(number) != 5 or not number.isdigit():
        print("Ошибка: введите строго пятизначное положительное целое число.")
    else:
        # Преобразуем символы в отдельные цифры
        d10k = int(number[0])   # десятки тысяч
        d1k = int(number[1])    # тысячи
        hundreds = int(number[2])
        tens = int(number[3])
        ones = int(number[4])

        # Шаг 1: десятки в степени единиц
        power = tens ** ones

        # Шаг 2: умножаем на количество сотен
        mult = power * hundreds

        # Шаг 3: делим на разность десятков тысяч и тысяч
        diff = d10k - d1k

        # Защита от деления на 0
        if diff == 0:
            print("Ошибка: деление на ноль.")
        else:
            result = mult / diff
            print("Результат:", float(result))
